# memcopy.py
data = [
    # MOVEA.L #0x2000, A0  ; Source address
    0x20, 0x7C, 0x00, 0x00, 0x20, 0x00,
    
    # MOVEA.L #0x3000, A1  ; Destination address
    0x22, 0x7C, 0x00, 0x00, 0x30, 0x00,
    
    # MOVE.W #100, D0      ; Counter (100 bytes)
    0x30, 0x3C, 0x00, 0x64,
    
    # Loop: MOVE.B (A0)+, (A1)+
    0x12, 0xD8,
    
    # DBRA D0, loop (decrement and branch)
    0x51, 0xC8, 0xFF, 0xFC,
    
    # STOP
    0x4E, 0x72, 0x27, 0x00
]

with open('memcopy.bin', 'wb') as f:
    f.write(bytes(data))
